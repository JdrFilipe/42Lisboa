#include <stdlib.h>

int		ft_strlen(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *dst;

	dst = malloc(ft_strlen(src) + 1);
	if (!dst)
		return (0);
	if (ft_strlen(src) < 2)
		return(0);
	ft_strcpy(dst, src);
	return (dst);
}
