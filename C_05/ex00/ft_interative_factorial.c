int		ft_interative_factorial(int nb)
{
	int j;

	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0); 
	j = 1;
	while (nb > 1)
	{
		j *= nb;
		nb--;
	}
	return (j);
}
